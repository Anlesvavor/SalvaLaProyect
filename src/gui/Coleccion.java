    private void mntAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mntAbrirActionPerformed
        Juego j = new Juego();
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
                "Inventario de Juegos *.inj", "inj");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(null);
        ObjectInputStream entrada = null;
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                
                System.out.println("You chose to open this file: "
                        + chooser.getSelectedFile().getName());
                entrada = new ObjectInputStream(new FileInputStream(chooser.getSelectedFile()));
                
                j = (Juego) entrada.readObject();
                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Coleccion.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Coleccion.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Coleccion.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                try {
                    entrada.close();
                } catch (IOException ex) {
                    Logger.getLogger(Coleccion.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
        lblNombre.setText(j.getNombre());
        lblClasificacion.setText(j.getClasificacion());
        lblRuta.setText(j.getRuta());
        txtNotas.setText(j.getNotas());
    }//GEN-LAST:event_mntAbrirActionPerformed

